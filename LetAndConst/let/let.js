/***************************************************
1.let用来声明变量，但所声明的变量，只在let命令所在的代码块内有效。
2.不存在变量提升。变量一定要在声明后使用。
3.暂时性死区：只要块级作用域内存在let命令，它所声明的变量就“绑定”这个区域，不再受外部的影响。
4.不允许重复声明。
***************************************************/
var a = [];
for(var i = 0; i < 10; i++){
    a[i] = () => console.log(i);
}
a[6]();
var a = [];
for(let i = 0; i < 10; i++){
    a[i] = () => console.log(i);
}
a[6]();
